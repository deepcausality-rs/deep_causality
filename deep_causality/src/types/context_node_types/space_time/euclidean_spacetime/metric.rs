use crate::{EuclideanSpacetime, Metric};

impl Metric<f64> for EuclideanSpacetime {
    fn distance(&self, other: &Self) -> f64 {
        let dx = self.x - other.x;
        let dy = self.y - other.y;
        let dz = self.z - other.z;
        (dx * dx + dy * dy + dz * dz).sqrt()
    }
}
